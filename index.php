// Animate elements when they come into view
function setupScrollAnimations() {
  // Section titles
  gsap.utils.toArray('.section-title').forEach(title => {
    gsap.from(title, {
      scrollTrigger: {
        trigger: title,
        start: "top 80%",
        toggleActions: "play none none none"
      },
      opacity: 0,
      y: 50,
      duration: 0.8,
      ease: "power2.out"
    });
  });
  
  // Section subtitles
  gsap.utils.toArray('.section-subtitle').forEach(subtitle => {
    gsap.from(subtitle, {
      scrollTrigger: {
        trigger: subtitle,
        start: "top 80%",
        toggleActions: "play none none none"
      },
      opacity: 0,
      y: 30,
      duration: 0.8,
      delay: 0.3,
      ease: "power2.out"
    });
  });
  
  // Product cards
  gsap.utils.toArray('.product-card').forEach((card, i) => {
    gsap.from(card, {
      scrollTrigger: {
        trigger: card,
        start: "top 80%",
        toggleActions: "play none none none"
      },
      opacity: 0,
      y: 50,
      duration: 0.6,
      delay: i * 0.1,
      ease: "back.out(1)"
    });
  });
  
  // Stats counter animation
  const counters = document.querySelectorAll('.counter');
  counters.forEach(counter => {
    const target = +counter.getAttribute('data-target');
    const suffix = counter.textContent.match(/%|\+/) ? counter.textContent.match(/%|\+/)[0] : '';
    
    ScrollTrigger.create({
      trigger: counter,
      start: "top 80%",
      onEnter: () => {
        gsap.to(counter, {
          innerText: target,
          duration: 2,
          ease: "power1.out",
          snap: { innerText: 1 },
          modifiers: {
            innerText: function(innerText) {
              return Math.floor(innerText) + suffix;
            }
          }
        });
      }
    });
  });
  
  // Craftsmanship features
  gsap.utils.toArray('.craftsmanship-feature').forEach((feature, i) => {
    gsap.from(feature, {
      scrollTrigger: {
        trigger: feature,
        start: "top 80%",
        toggleActions: "play none none none"
      },
      x: i % 2 === 0 ? -50 : 50,
      opacity: 0,
      duration: 0.8,
      delay: i * 0.1,
      ease: "power2.out"
    });
  });
  
  // Sustainability cards
  gsap.utils.toArray('.sustainability-card').forEach((card, i) => {
    gsap.from(card, {
      scrollTrigger: {
        trigger: card,
        start: "top 80%",
        toggleActions: "play none none none"
      },
      scale: 0.8,
      opacity: 0,
      duration: 0.6,
      delay: i * 0.1,
      ease: "back.out(1.7)"
    });
  });
  
  // Timeline items
  gsap.utils.toArray('.timeline-item').forEach((item, i) => {
    gsap.from(item, {
      scrollTrigger: {
        trigger: item,
        start: "top 80%",
        toggleActions: "play none none none"
      },
      x: i % 2 === 0 ? -100 : 100,
      opacity: 0,
      duration: 0.8,
      delay: i * 0.1,
      ease: "power2.out"
    });
  });
  
  // Instagram images
  gsap.utils.toArray('.instagram-img').forEach((img, i) => {
    gsap.from(img, {
      scrollTrigger: {
        trigger: img,
        start: "top 80%",
        toggleActions: "play none none none"
      },
      scale: 0,
      rotation: i % 2 === 0 ? -10 : 10,
      duration: 0.6,
      delay: i * 0.05,
      ease: "back.out(1.7)"
    });
  });
}

